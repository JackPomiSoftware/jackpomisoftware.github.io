let lang = localStorage.getItem('lang') || 'en';

function translate() {
  // Обычные текстовые переводы
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    el.textContent = translations[lang]?.[key] || el.textContent;
  });

  // Переводы с HTML
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.getAttribute('data-i18n-html');
    el.innerHTML = translations[lang]?.[key] || el.innerHTML;
  });

  // Смена флага
  const flagImg = document.getElementById('loc_flag');
  if (flagImg) {
    flagImg.src = `img/loc/${lang}.png`;
  }
}

document.getElementById('lang-switch').value = lang;
document.getElementById('lang-switch').addEventListener('change', (e) => {
  lang = e.target.value;
  localStorage.setItem('lang', lang);
  translate();
});

translate(); // применить при загрузке